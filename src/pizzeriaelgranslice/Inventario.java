package pizzeriaelgranslice;


public class Inventario 
{
 int salsas;
 int sal;
 int aceite;
 int jamon;
 int hongos;
 int harina;
 int piña;
 int chileDulce;
 int cebolla;
 int tomate;
 int pepperoni;

    public Inventario() {
    }

    public Inventario(int salsas, int sal, int aceite, int jamon, int hongos, int harina, int piña, int chileDulce, int cebolla, int tomate, int pepperoni) {
        this.salsas = salsas;
        this.sal = sal;
        this.aceite = aceite;
        this.jamon = jamon;
        this.hongos = hongos;
        this.harina = harina;
        this.piña = piña;
        this.chileDulce = chileDulce;
        this.cebolla = cebolla;
        this.tomate = tomate;
        this.pepperoni = pepperoni;
    }

    public int getSalsas() {
        return salsas;
    }

    public void setSalsas(int salsas) {
        this.salsas = salsas;
    }

    public int getSal() {
        return sal;
    }

    public void setSal(int sal) {
        this.sal = sal;
    }

    public int getAceite() {
        return aceite;
    }

    public void setAceite(int aceite) {
        this.aceite = aceite;
    }

    public int getJamon() {
        return jamon;
    }

    public void setJamon(int jamon) {
        this.jamon = jamon;
    }

    public int getHongos() {
        return hongos;
    }

    public void setHongos(int hongos) {
        this.hongos = hongos;
    }

    public int getHarina() {
        return harina;
    }

    public void setHarina(int harina) {
        this.harina = harina;
    }

    public int getPiña() {
        return piña;
    }

    public void setPiña(int piña) {
        this.piña = piña;
    }

    public int getChileDulce() {
        return chileDulce;
    }

    public void setChileDulce(int chileDulce) {
        this.chileDulce = chileDulce;
    }

    public int getCebolla() {
        return cebolla;
    }

    public void setCebolla(int cebolla) {
        this.cebolla = cebolla;
    }

    public int getTomate() {
        return tomate;
    }

    public void setTomate(int tomate) {
        this.tomate = tomate;
    }

    public int getPepperoni() {
        return pepperoni;
    }

    public void setPepperoni(int pepperoni) {
        this.pepperoni = pepperoni;
    }
 
    
    
    
    
    
    
    
}
