package pizzeriaelgranslice;


public class Pizzas 
{
 int pepperoni;
 int jamon;
 int hawaiana;
 int suprema;
 int jamonHongos;

    public Pizzas() {
    }

    public Pizzas(int pepperoni, int jamon, int hawaiana, int suprema, int jamonHongos) {
        this.pepperoni = pepperoni;
        this.jamon = jamon;
        this.hawaiana = hawaiana;
        this.suprema = suprema;
        this.jamonHongos = jamonHongos;
    }

    public int getPepperoni() {
        return pepperoni;
    }

    public void setPepperoni(int pepperoni) {
        this.pepperoni = pepperoni;
    }

    public int getJamon() {
        return jamon;
    }

    public void setJamon(int jamon) {
        this.jamon = jamon;
    }

    public int getHawaiana() {
        return hawaiana;
    }

    public void setHawaiana(int hawaiana) {
        this.hawaiana = hawaiana;
    }

    public int getSuprema() {
        return suprema;
    }

    public void setSuprema(int suprema) {
        this.suprema = suprema;
    }

    public int getJamonHongos() {
        return jamonHongos;
    }

    public void setJamonHongos(int jamonHongos) {
        this.jamonHongos = jamonHongos;
    }
    
 
  
 
 
 
 
 
    
}
